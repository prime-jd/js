const ch = 4; 
switch (ch) {    //it can also take string values
    case 1:
        console.log("hey it's 1")
        break;
    case 2:
        console.log("hey it's 2")
        break;
    case 3:
        console.log("hey it's 3")
        break;
    case 4:
        console.log("hey it's 4")
        break;
    default:
        console.log("jai shree ram")
        break;
}
